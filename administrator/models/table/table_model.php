class Table_Model extends Model {
	
	function Table_Model () {
		parent::Model();
	}
	
	function table_list() {
		$query = $this->db->get('sources');
		return $query->result();
	}
	
	function list_one($id) {
		$query = $this->db->get_where('sources', array('id' => $id ));
		return $query->result();
	}
}
